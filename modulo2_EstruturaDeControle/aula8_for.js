/* Laço de Repetição FOR  */
// for significa para 

// Usando While.

let n1= 1 
 while (n1<=10) {
     console.log (`Contador ${n1}`)
     n1++

 }

 // Usando for .
for (let numero = 1; numero <= 10; numero++) {
console.log( `O número é ${numero}`)
}

 // Navegando dentro de um array 

 const notas = [10, 8.9, 7.4, 5]
 for (let n1=0; n1< notas.length; n1++) {   // length significa comprimento .
     console.log(`Suas Notas são ${notas[n1]}`)
 }



