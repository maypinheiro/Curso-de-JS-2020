// Operadores de Atribuição

// Usamos o sinal seguido de = . 

const a = 1 
let b = 3

b += a  // Atribuição de adição  ( é o mesmo q a +b )
console.log(b)

b-=2 // Atribuição de subtração estou diminuindo ( é o mesmo q a -b )
console.log(b)

b*=2 //Atribuição mutiplicativa ( é o mesmo que b * b)
console.log(b)

b/=2 //Atribuição divição (é o mesmo q b/2)
console.log(b)

b%=2 // Atribuição de modulo ( é o Resto da Divisão b% )
console.log(b)
